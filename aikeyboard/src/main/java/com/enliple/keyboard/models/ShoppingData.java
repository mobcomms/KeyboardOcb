package com.enliple.keyboard.models;

public class ShoppingData {
    String imgUrl;
    String linkUrl3;
    String totalSaveRate;
    String mallName;
    String linkType;

    public String getLinkType() {
        return linkType;
    }

    public void setLinkType(String linkType) {
        this.linkType = linkType;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getLinkUrl3() {
        return linkUrl3;
    }

    public void setLinkUrl3(String linkUrl3) {
        this.linkUrl3 = linkUrl3;
    }

    public String getTotalSaveRate() {
        return totalSaveRate;
    }

    public void setTotalSaveRate(String totalSaveRate) {
        this.totalSaveRate = totalSaveRate;
    }

    public String getMallName() {
        return mallName;
    }

    public void setMallName(String mallName) {
        this.mallName = mallName;
    }



}
